{
  return (nb1 + nb2);
}

int		my_sub(int nb1, int nb2)
{
  return (nb1 - nb2);
}

int		my_mul(int nb1, int nb2)
{
  return (nb1 * nb2);
}

int		my_div(int nb1, int nb2)
{
  return (nb1 / nb2);
}

int		my_mod(int nb1, int nb2)
{
  return (nb1 % nb2);
}
